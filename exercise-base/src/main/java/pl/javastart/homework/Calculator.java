public class Calculator {

  double add(double a, double b) {
    double result = a + b;
    return result;
  }

  double subtract(double a, double b) {
    double result = a - b;
    return result;
  }

  double multiply(double a, double b) {
    double result = a * b;
    return result;
  }

  double divide(double a, double b) {
    double result = a / b;
    return result;
  }
}
