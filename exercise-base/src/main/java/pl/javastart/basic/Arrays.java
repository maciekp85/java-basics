public class Arrays {

  public static void main(String[] args) {

    // Create numbers array (default filled in zeros)
    int[] numbers = new int[5];

    // filling in values into following cells
    numbers[0] = 1;
    numbers[1] = 5;
    numbers[2] = 10;
    numbers[3] = 15;
    numbers[4] = 20;

    // printing entered values in the same way
    System.out.println(numbers[0]);
    System.out.println(numbers[4]);
  }
}
